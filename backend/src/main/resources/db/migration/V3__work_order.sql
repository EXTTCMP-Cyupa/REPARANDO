    id UUID PRIMARY KEY,
    client_id UUID NOT NULL,
    worker_id UUID NOT NULL,
    status VARCHAR(30) NOT NULL,
    CONSTRAINT fk_work_order_worker FOREIGN KEY (worker_id) REFERENCES worker_account (id)
);

